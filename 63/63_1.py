class Solution:   
   
    def travel(self, row, column, obstacleGrid, max_row, max_col):
        if row == max_row - 1 and column == max_col - 1:
            if obstacleGrid[row][column]:
                return 0
            return 1
        
        travel_count = 0
        if row + 1 < max_row and not obstacleGrid[row+1][column]:
            travel_count += self.travel(row + 1, column, obstacleGrid, max_row, max_col)
        if column + 1 < max_col and not obstacleGrid[row][column+1]:
            travel_count += self.travel(row, column + 1, obstacleGrid, max_row, max_col)

        return travel_count
    
    def uniquePathsWithObstacles(self, obstacleGrid):
        max_row = len(obstacleGrid)
        max_col = len(obstacleGrid[0])
        
        if max_row == 0 or max_col == 0:
            return 0
        if obstacleGrid[0][0]:
            return 0
        
        return self.travel(0, 0, obstacleGrid, max_row, max_col)   
        
